/*!
 \brief  Determines in the LUT index into a 1x512 lookup table
 \par    Detailed Description:
         This function maps a 3x3 sliding window pinned at the current pixel into a lookup table (LUT).
		 The lookup table is a 1x512 table, where then table length is determined by the number of elements
		 in the sliding window (9).  pow(2, 9) = 512
 \param  pData  - the input data image
 \param  row    - the current row
 \param  col    - the current column
 \return The LUT index
 \remark Assumes pData is valid for speed purposes since it is an internal routine.
         This function also assumes the data pixel is a byte in size.
 \author JAH
 \date   2009-02-19
 */
INT16 BW_Nhood3Offset(BAL_sImage *pData, UINT32 row, UINT32 col) {
	INT32 minR, maxR, minC, maxC;
	INT32 rr,cc;
	BYTE  value;
	INT32 rowOffset;
	INT16 result  = 0;

	// Determine the neighborhood extent that does not exceed image boundaries
	minR = (row == 0) ? 1 : 0;
	maxR = (row == (pData->height - 1)) ? 1 : 2;
	minC = (col == 0) ? 1 : 0;
	maxC = (col == (pData->width - 1)) ? 1 : 2;

	
	for (rr=minR; rr <= maxR; rr++) {
		//rowOffset = (row+rr-1)*pData->width;
		for (cc=minC; cc <= maxC; cc++) {
			value   = pData->scan0[(col + cc - 1) * pData->height + row + rr -1];
			result += g_Weights3[rr][cc]*(value != 0);
		} // end for cc
	} // end for rr

	return (result);
} // end BW_Nhood3Offset



